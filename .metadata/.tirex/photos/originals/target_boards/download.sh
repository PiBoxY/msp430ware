#!/usr/bin/env bash
#
# Try and download target board images

TARGET_BOARDS="MSP-TS430D8 MSP-TS430DA38 MSP-TS430DL48 MSP-TS430DW28 MSP-TS430L092 MSP-TS430PEU128 MSP-TS430PM64 MSP-TS430PM64A MSP-TS430PM64D MSP-TS430PM64F MSP-TS430PN80 MSP-TS430PN80A MSP-TS430PN80USB MSP-TS430PW14 MSP-TS430PW24 MSP-TS430PW28 MSP-TS430PW28A MSP-TS430PZ100 MSP-TS430PZ100A MSP-TS430PZ100AUSB MSP-TS430PZ100B MSP-TS430PZ100C MSP-TS430PZ100D MSP-TS430PZ100USB MSP-TS430PZ5x100 MSP-TS430QFN23x0 MSP-TS430RGC64B MSP-TS430RGC64C MSP-TS430RGC64USB MSP-TS430RGZ48B MSP-TS430RGZ48C MSP-TS430RHA40A MSP-TS430RHB32A MSP-TS430RSB40 MSP-TS432PZ100"

for board in ${TARGET_BOARDS}; do
    #lower=$(echo "${board}" | tr '[:upper:]' '[:lower:]')
    removed=$(echo "${board}" | sed -e 's/MSP-//g')
    wget http://www.ti.com/graphics/tool/${removed}.jpg
done
