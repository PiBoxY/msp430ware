/*
 * HOW TO CREATE YOUR OWN VOLUME IN 'C' BELOW
 *
 1. Replace the storageVol[] below with a blank one:  const WORD storageVol[];
 2. Rebuild, and connect to the host
 3. Allow the host to format the volume
 4. Copy the files into volume that you want to be present inside storageVol[]
 5. While still in emulation mode, open CCS' "Memory Browser"
 6. Use the "Save Memory" feature to create a *.dat file of the flash memory
    contents between 0x10000-24400, in 16-bit TI hex style.  (Segment
    MYDRIVE occupies all flash memory from 0x10000 to the end of the map.)
 7. Open this *.dat as a text file, and re-format its contents to look like
    storageVol[] below.  (You may want to first save a *.dat file of storageVol
    below, and compare the *.dat file against the C text below, to understand
    the re-formatting.)  It's a lot of text -- you might want to use AutoHotkey
    or a Python script.
 8. Rebuild and execute
 */


